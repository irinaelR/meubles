CREATE OR REPLACE VIEW MEUBLEAVECPRIXMATERIEL AS (
    SELECT
        PRIX, IDCATEGORIE, IDSTYLE, IDVOLUME,
        NOMCATEGORIE, NOMSTYLE, NOMMATIERE,
        FORMULEAVECSTYLECATEGORIE.IDMATIERE, QUANTITE , NOMVOLUME
