CREATE OR REPLACE VIEW RESTESTOCK AS (
    SELECT 
        MOUVEMENTSTOCK.IDMATIERE, MATIERE.NOMMATIERE, SUM(ENTREE)-SUM(SORTIE) AS RESTE
        FROM MOUVEMENTSTOCK JOIN MATIERE
