CREATE OR REPLACE VIEW FORMULEAVECSTYLECATEGORIE AS (
    SELECT
        TAILLEPARCATEGORIEPARSTYLE.IDTCS,TAILLEPARCATEGORIEPARSTYLE.IDCATEGORIE, TAILLEPARCATEGORIEPARSTYLE.IDSTYLE, TAILLEPARCATEGORIEPARSTYLE.IDVOLUME,
